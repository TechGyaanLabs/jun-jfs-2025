package com.careerit.jfs.cj.day4;

public class UnaryOperators {

    public static void main(String[] args) {
        // Unary operators in Java ++, --
        int a = 10;
        a++;
        System.out.println("After incrementing a: " + a);
        int b = 20;
        b--;
        System.out.println("After decrementing b: " + b);
    }
}
