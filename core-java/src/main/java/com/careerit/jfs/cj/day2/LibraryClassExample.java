package com.careerit.jfs.cj.day2;

public class LibraryClassExample {

    public static void main(String[] args) {

            double res = Math.sqrt(25);
            double max = Math.max(10, 20);
            double pow = Math.pow(2, 3);

            System.out.println("Square root of 25 is: " + res);
            System.out.println("Maximum of 10 and 20 is: " + max);
            System.out.println("2 raised to the power of 3 is: " + pow);
    }
}
